void	ft_rev_int_tab(int *tab, int size)
{
	int	temp;
	int	k;

	k = 0;
	while (k < (size / 2))
	{
		temp = *(tab + k);
		*(tab + k) = *(tab + size - k - 1);
		*(tab + size - k - 1) = temp;
		k++;
	}
}
