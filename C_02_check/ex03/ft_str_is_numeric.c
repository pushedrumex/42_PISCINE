int	ft_str_is_numeric(char *str)
{
	int	len;
	int	i;

	len = 0;
	while (str[len] != '\0')
	{
		len++;
	}
	if (len == 0)
		return (1);
	i = 0;
	while (i < len)
	{
		if ('0' > str[i] || str[i] > '9')
			return (0);
		i++;
	}
	return (1);
}
